async function main() {
  console.log(`Seeding ${DEV_CASES.length} dev cases...\n`)

  for (const c of DEV_CASES) {
    await prisma.case.upsert({
      where: { slug: c.slug },
      update: c,
      create: c,
    })
    console.log(`  ✓ ${c.slug}  ${c.client.padEnd(28)} ${c.title}`)
  }

  const totalPublic = await prisma.case.count({ where: { isPublic: true } })
  const devPublic = await prisma.case.count({ where: { isPublic: true, type: 'DEV' } })
  const videoPublic = await prisma.case.count({ where: { isPublic: true, type: 'VIDEO' } })

  console.log(`\nTotal public: ${totalPublic}  ·  DEV: ${devPublic}  ·  VIDEO: ${videoPublic}`)
}

main()
  .catch((e) => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
