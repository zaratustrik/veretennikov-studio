async function main() {
  for (const a of articles) {
    const data = {
      title: a.title,
      excerpt: a.excerpt,
      body: a.body,
      tags: a.tags,
      coverUrl: a.coverUrl,
      author: AUTHOR,
      isPublic: true,
      publishedAt: a.publishedAt,
    }
    const post = await prisma.post.upsert({
      where: { slug: a.slug },
      create: { slug: a.slug, ...data },
      update: data,
    })
    console.log(`upserted: /blog/${post.slug}`)
  }
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
