async function main() {
  console.log(`Updating ${UPDATES.length} dev cases with NDA-safe descriptions...\n`)

  for (const u of UPDATES) {
    const result = await prisma.case.update({
      where: { slug: u.slug },
      data: {
        client: u.client,
        title: u.title,
        description: u.description,
        challenge: u.challenge,
        solution: u.solution,
        outcome: u.outcome,
        services: u.services,
      },
    })
    console.log(`  ✓ ${result.slug.padEnd(22)} ${result.client}`)
  }

  console.log('\nDone.')
}

main()
  .catch((e) => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
